package starvationevasion.greninja.clientCommon;

/**
 * CSS Styles to be used on gui.
 */
public class GuiStyles
{
  /**
   * Light gray background padded with 4 pixels.
   * @return        CSS representation of formatting.
   */
  public static String paddedGray()
  {
    return   "-fx-background-color: lightgray; -fx-padding: 4px;";
  }
}
