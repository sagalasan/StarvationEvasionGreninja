package starvationevasion.sim.io;

/**
 * Different log levels the logger can use
 *
 * ALL: log all messages for any log level
 * DEBUG: Only log messages marked as DEBUG
 * ERROR: Only log messages marked as ERROR
 * WARNING: Only log messages marked as WARNING
 * INFO: Only log messages marked as INFO
 * OFF: Do not log any messages
 */
public enum LogLevel
{
  ALL,
  DEBUG,
  ERROR,
  WARNING,
  INFO,
  OFF,
}
